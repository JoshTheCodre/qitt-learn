/**
 * Bakes the flat 2D colour icons into inline data URIs (lib/iconflat.generated.ts).
 *
 * Source is Fluent Emoji's *Color* variant — flat, 2D, genuinely multi-colour (15-34
 * colours each), as opposed to the monochrome Material Symbols glyphs, which render as
 * thin outlines and read as inactive.
 *
 * The sources are 12-26KB of SVG each (~100KB for the set). Inlined as raw SVG that
 * would be serialized into every page twice (markup + RSC payload), so they're
 * rasterized to 96px WebP first — a few KB each, and they paint with the HTML instead
 * of arriving after first paint.
 *
 * Re-run after adding or changing an icon in public/icons/flat/:
 *   npm run icons
 */
import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import sharp from "sharp";

const SRC_DIR = path.join(process.cwd(), "public", "icons", "flat");
const OUT_FILE = path.join(process.cwd(), "lib", "iconflat.generated.ts");
const SIZE = 96; // 4x the largest render size, so it stays crisp on retina

/*
 * Per-icon hue shift, so the set lands on a cool palette (violet / teal / green /
 * blue) without losing the gloss.
 *
 * Rotating the hue keeps every gradient, highlight and shadow intact — it only moves
 * the colour around the wheel. Hand-recolouring the SVGs would have meant flattening
 * those gradients, which is exactly the shine we want to keep.
 *
 * `hue` is a rotation in degrees from the artwork's own hue, not an absolute target.
 * `sat` tames the result where a straight rotation lands somewhere neon.
 */
const TINT = {
  // envelope: pink (337°) -> violet
  request: { hue: 298, sat: 0.95 },
  // folder: amber (41°) -> teal. Full saturation here goes neon cyan.
  contribute: { hue: 139, sat: 0.65 },
  // green book and the chart are already cool — leave them alone.
  practice: { hue: 0, sat: 1 },
  performance: { hue: 0, sat: 1 },
  study: { hue: 0, sat: 1 },
};

const files = (await readdir(SRC_DIR)).filter((f) => f.endsWith(".svg")).sort();

const entries = [];
for (const file of files) {
  const name = path.basename(file, ".svg");
  const input = await readFile(path.join(SRC_DIR, file));
  const tint = TINT[name] ?? { hue: 0, sat: 1 };

  let pipe = sharp(input, { density: 384 }).resize(SIZE, SIZE, {
    fit: "contain",
    background: { r: 0, g: 0, b: 0, alpha: 0 },
  });
  if (tint.hue || tint.sat !== 1) {
    pipe = pipe.modulate({ hue: tint.hue, saturation: tint.sat });
  }
  const webp = await pipe.webp({ quality: 92, effort: 6 }).toBuffer();

  entries.push({ name, uri: `data:image/webp;base64,${webp.toString("base64")}` });
  console.log(
    `${name.padEnd(12)} ${(input.length / 1024).toFixed(1).padStart(6)}KB -> ${(webp.length / 1024).toFixed(1).padStart(5)}KB`
  );
}

const body = `// GENERATED by scripts/build-iconflat.mjs — do not edit by hand.
// Source: public/icons/flat/*.svg, rasterized to ${SIZE}px and inlined as WebP.

export type IconFlatName =
${entries.map((e) => `  | "${e.name}"`).join("\n")};

export const ICON_FLAT: Record<IconFlatName, string> = {
${entries.map((e) => `  ${e.name}: "${e.uri}",`).join("\n")}
};
`;

await writeFile(OUT_FILE, body, "utf8");

const total = entries.reduce((n, e) => n + e.uri.length, 0);
console.log(
  `\nWrote ${entries.length} icons to lib/iconflat.generated.ts (~${(total / 1024).toFixed(1)}KB inlined)`
);
